--CALCULO TOTAL DE VENDAS
SET TERM ^^ ;
CREATE PROCEDURE SP_CALCULAR_TOTAL_VENDA (
    PID_VENDA INTEGER
)
RETURNS (
    TOTAL DECIMAL(10,2)
)
AS
BEGIN
  SELECT SUM(SUBTOTAL)
  FROM TBL_ITENS_VENDA
  WHERE ID_VENDA = :PID_VENDA
  INTO :TOTAL;
  SUSPEND;
END ^^
SET TERM ; ^^

